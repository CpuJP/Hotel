    private void jButton_CargarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CargarDatosActionPerformed
        // TODO add your handling code here:
        covidMedicoController cmc = new covidMedicoController();
        List<covidMedico> antecedentes = cmc.getCovidMedicos();
        DefaultTableModel m = (DefaultTableModel) jTable_Antecedentes.getModel();
        int totalFilas = m.getRowCount();
        for (int i = totalFilas -1; i >= 0; i--) {
            m.removeRow(i);
        }
        for (covidMedico cm : antecedentes){
            m.addRow(new Object[]{cm.getContagio(), cm.getFechaContagio(), cm.getVacunado(),
                cm.getFechaVacunado(), cm.getMedicoId(), cm.getMedicoCentroMedico()});
        }
    }//GEN-LAST:event_jButton_CargarDatosActionPerformed

