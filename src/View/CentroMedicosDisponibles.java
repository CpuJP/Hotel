    private void jButton_CargarInformacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CargarInformacionActionPerformed
        // TODO add your handling code here:
        centroMedicoController mcc = new centroMedicoController();
        List<centroMedico> centrosMedicos = mcc.getCentrosMedicos();
        DefaultTableModel m = (DefaultTableModel) jTable_CentrosMedicos.getModel();
        int totalFilas = m.getRowCount();
        for (int i = totalFilas -1; i >= 0; i--) {
            m.removeRow(i);
        }
        for (centroMedico cm : centrosMedicos){
            m.addRow(new Object[]{cm.getNit(), cm.getNombreCentroMedico(), cm.getTelefono(),
                cm.getEmail(), cm.getNivel()});
        }
    }//GEN-LAST:event_jButton_CargarInformacionActionPerformed

