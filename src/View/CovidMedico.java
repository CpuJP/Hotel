    private void jButton_CargarSedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CargarSedeActionPerformed
        // TODO add your handling code here:
        medicoController mc = new medicoController();
        String cedula = jTextField_ID.getText();
        medico m = mc.getMedicoById(cedula);
        if (m.getId() == null) {
            JOptionPane.showMessageDialog(null, "No hay ningún médico registrado con la cédula: "+jTextField_ID.getText());
        }
        jTextField_SedeTrabajo.setText(m.getCentroMedico());
    }//GEN-LAST:event_jButton_CargarSedeActionPerformed

    private void jButton_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarActionPerformed
        // TODO add your handling code here:
        try {
            covidMedicoController cmc = new covidMedicoController();
            String contagio = jComboBox_Contagio.getSelectedItem().toString();
            String fechaContagio = jComboBox_AñoContagio.getSelectedItem().toString()+"-"
                    +jComboBox_MesContagio.getSelectedItem().toString()+"-"+jComboBox_DiaContagio.getSelectedItem().toString();
            String vacunado = jComboBox_Vacunado.getSelectedItem().toString();
            String fechaVacunado = jComboBox_AñoVacuna.getSelectedItem().toString()+"-"+
                    jComboBox_MesVacuna.getSelectedItem().toString()+"-"+jComboBox_DiaVacuna.getSelectedItem().toString();
            String idMedico = jTextField_ID.getText();
            String sede = jTextField_SedeTrabajo.getText();
            covidMedico cm = new covidMedico(contagio, fechaContagio, vacunado, fechaVacunado, idMedico, sede);
            cmc.saveCovidMedico(cm);
            JOptionPane.showMessageDialog(null, "El antecedente se creó con éxito");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al crear el antecedente, verifique los datos");
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_RegistrarActionPerformed

